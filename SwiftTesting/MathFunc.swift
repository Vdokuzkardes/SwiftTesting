import Foundation

class MathFunc{
    
    func Sum(x: Int, y:Int) -> Int{
        return x + y
    }
    
    func multiply(x: Int, y:Int) -> Int{
        return x * y
    }
    
    func divide(x: Int, y:Int) -> Int{
        return x / y
    }
    
}
